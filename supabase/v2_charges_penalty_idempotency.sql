-- OutWay CRM — идемпотентность ежедневного начисления пени
-- Запустить в Supabase SQL Editor, затем передеплоить edge-функцию daily-penalty.
--
-- Проблема: daily-penalty можно вызвать (случайно повторно или так как функция
-- задеплоена с --no-verify-jwt — вызвать может кто угодно) несколько раз за
-- один день, и каждый вызов прибавлял ещё +100 сом к пене (до потолка 15%).
-- Это добавляет колонку с датой последнего начисления пени, чтобы повторный
-- вызов в тот же день ничего не менял.

alter table public.v2_charges
  add column if not exists penalty_last_charged_on date;
